function apply_amp_masks(model)
vga_path = [model '/RF_VGA'];
pa_path = [model '/RF_PA'];

try Simulink.Mask.get(vga_path).delete(); catch; end
set_param(vga_path, 'Source_linear_gain', 'Available power gain');
set_param(vga_path, 'linear_gain', 'rf_vga_gain_db');
set_param(vga_path, 'NF', 'rf_vga_nf_db');
set_param(vga_path, 'OpenFcn', 'ex18_amp_dialog(gcb);');
fprintf('RF_VGA: double-click to edit gain.\n');

try Simulink.Mask.get(pa_path).delete(); catch; end
set_param(pa_path, 'Source_linear_gain', 'Available power gain');
set_param(pa_path, 'linear_gain', 'rf_pa_gain_db');
set_param(pa_path, 'NF', 'rf_pa_nf_db');
set_param(pa_path, 'OpenFcn', 'ex18_amp_dialog(gcb);');
