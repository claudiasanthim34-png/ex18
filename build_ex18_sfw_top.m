    '土壤模型 FIR：由均匀有损土壤、地表反射、弱杂波和管道目标频响转换得到。');
end

function clear_subsystem(subsystem_path)
try
    Simulink.SubSystem.deleteContents(subsystem_path);
catch
    blocks = find_system(subsystem_path, 'SearchDepth', 1, 'Type', 'Block');
    for idx = 2:numel(blocks)
        delete_block(blocks{idx});
    end
end
end

