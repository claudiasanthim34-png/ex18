function build_ex18_sfw_top()
%BUILD_EX18_SFW_TOP 重新生成 ex18 的 RF Blockset 步进频率前端模型。

work_dir = fileparts(mfilename('fullpath'));
old_dir = pwd;
cleanup = onCleanup(@() cd(old_dir));
cd(work_dir);
addpath(work_dir);
addpath(fullfile(work_dir, 'filterlib'));
addpath(fullfile(work_dir, 'dialogs'));

setup_ex18_sfw();

model = 'ex18_sfw_top';
reset_model(model);
configure_model(model);

add_sfw_source(model);
add_source_monitor(model);
add_rf_chain(model);
add_rf_sinks(model);
add_gpr_receiver(model);
apply_filter_masks(model);
connect_model(model);
add_note(model);

save_system(model, fullfile(work_dir, [model '.slx']));
fprintf('Built %s.slx in %s\n', model, work_dir);
end

function reset_model(model)
if bdIsLoaded(model)
    close_system(model, 1);
end
new_system(model);
end

function configure_model(model)
init_cmd = [ ...
    'model_dir = fileparts(get_param(bdroot, ''FileName''));' ...
    'if ~isempty(model_dir), addpath(model_dir); addpath(fullfile(model_dir, ''filterlib'')); addpath(fullfile(model_dir, ''dialogs'')); end;' ...
    'setup_ex18_sfw;' ...
    ];

set_param(model, ...
    'StartTime', '0.0', ...
    'StopTime', 'sfw_stop_s', ...
    'SolverType', 'Fixed-step', ...
    'Solver', 'FixedStepDiscrete', ...
    'FixedStep', 'sfw_sample_s', ...
    'InitFcn', init_cmd, ...
    'SaveFormat', 'StructureWithTime', ...
    'ReturnWorkspaceOutputs', 'off');
end

