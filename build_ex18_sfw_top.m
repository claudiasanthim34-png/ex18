    'CoefSource', 'Dialog parameters', ...
    'Coefficients', 'gpr_soil_fir', ...
    'InitialStates', '0', ...
    'SampleTime', 'sfw_sample_s', ...
    'InputProcessing', 'Elements as channels (sample based)', ...
    'Position', [190 118 385 192]);

add_line(subsystem_path, 'In1/1', 'GPR_Channel_FIR/1', 'autorouting', 'on');
add_line(subsystem_path, 'GPR_Channel_FIR/1', 'Out1/1', 'autorouting', 'on');

